int tbook[30][50] = {0,};
 
class Solution {
private:
	
	
public:
	vector<char> path;
	vector<char> ret;
	void dfs(int row, int col) {
		if (row < 0 || row > 29 || col < 0 || col > 49) return;
		if (tmap[row][col] == 1 || tbook[row][col] == 1) return;
		cout << row << ' ' << col << '\n';
		if (row == 29 && col == 49) {
			if (path.size() < ret.size()) {
				ret.resize(path.size());
				for (int i = 0; i < ret.size(); i++) {
					ret[i] = path[i];
				}
			}
		}
		tbook[row][col] = 1;
		path.push_back('D');
		dfs(row + 1, col);
		path.pop_back();
		
		path.push_back('L');
		dfs(row, col - 1);
		path.pop_back();
		
		path.push_back('R');
		dfs(row, col + 1);
		path.pop_back();
		
		path.push_back('U');
		dfs(row - 1, col);
		path.pop_back();
		tbook[row][col] = 0;
	}		
};



int main(void) {
	Solution s;	
	s.dfs(0, 0);
	for (int i = 0; i < s.ret.size(); i++) {
		cout << s.ret[i];
	}
	return 0;
}

